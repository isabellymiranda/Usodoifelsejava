public class Usoifeelse {
    public static void main (String[] args) {
        int valor1 = 1;
        int valor2 = 2;

        System.out.println((valor1 > valor2 ? "1 é Maior" : "2 é Maior"));

        if (valor1 > valor2) {
            System.out.println("1 é Maior");
        } else {
            System.out.println("2 é Maior");
        }
        
    }
}